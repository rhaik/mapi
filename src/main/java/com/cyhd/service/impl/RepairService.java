    /**
     * 是不是新用户
     * @param userId
     * @return
     */
    public boolean isNewInvitedUser(int userId){
        return invitedUsers.containsKey(userId);
    }

    /**
     * 是不是当前邀请人
     * @param user
     * @return
     */
    public boolean isInvitor(User user){
        return invitorUsers.contains("" + user.getUser_identity()) || invitorUsers.contains(user.getInvite_code());
    }

    /**
     * 获取其邀请人
     * @param userId
     * @return
     */
    public User getInvitor(int userId){
        String invite_code = invitedUsers.get(userId);
        //处理用户邀请的逻辑
        User invitor = null;
        if (invite_code.length() == 32) {
            invitor = userService.getUserByInviteCode(invite_code);
        }else if (invite_code.length() == 8){
            invitor = userService.getUserByIdentifyId(NumberUtil.safeParseInt(invite_code));
        }

        if (invitor == null || invitor.getId() >= userId) {
            invitor = null;
        }
        return invitor;
    }
